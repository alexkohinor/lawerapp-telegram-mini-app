/**
 * Получить источники права для категории
 */
export function getLegalSources(category: string): LegalSource[] {
  return LEGAL_SOURCES[category] || [];
}

/**
 * Получить определение правового термина
 */
export function getLegalTermDefinition(term: string): string | undefined {
  return COMMON_LEGAL_TERMS[term.toLowerCase() as keyof typeof COMMON_LEGAL_TERMS];
}

/**
 * Получить информацию о процедуре
 */
export function getLegalProcedure(procedure: string): string | undefined {
  return LEGAL_PROCEDURES[procedure.toLowerCase() as keyof typeof LEGAL_PROCEDURES];
}

/**
 * Получить срок исковой давности
 */
export function getStatuteOfLimitations(category: string): string {
  return STATUTE_OF_LIMITATIONS[category as keyof typeof STATUTE_OF_LIMITATIONS] || STATUTE_OF_LIMITATIONS['общий срок исковой давности'];
}
